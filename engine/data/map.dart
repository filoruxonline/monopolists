import 'package:hive/hive.dart';

part 'map.g.dart';

@HiveType(typeId: 2)
class Tile extends HiveObject {
  @HiveField(0)
  TileType type = TileType.land;
  @HiveField(1)
  int color;
  @HiveField(2)
  String id;
  @HiveField(3)
  String name;
  @HiveField(4)
  int price;
  @HiveField(5)
  int hyp;
  @HiveField(6)
  int housePrice;
  @HiveField(7)
  List<int> rent;
  @HiveField(8)
  bool sold = false;
  @HiveField(9)
  int level = 0;

  Tile(this.type,
      {this.color,
      this.id,
      this.name,
      this.price,
      this.housePrice,
      this.rent,
      this.hyp});
}

